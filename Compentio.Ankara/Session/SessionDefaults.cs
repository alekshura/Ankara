namespace Compentio.Ankara.Session
{
    /// <summary>
    /// Represents defaults for the Session.
    /// </summary>
    public static class SessionDefaults
    {
        /// <summary>
        /// Represent the default cookie name, which is "Session".
        /// </summary>
        public static readonly string CookieName = "Session";
        
        /// <summary>
        /// Represents the default path used to create the cookie, which is "/".
        /// </summary>
        public static readonly string CookiePath = "/";
    }
}